import Foundation

extension UIColor {
    class func hRedColor() -> UIColor {
        return UIColor(red: 194.0/255.0, green: 35.0/255.0, blue: 65.0/255.0, alpha: 1.0)
    }
    
    class func hLightGreyColor() -> UIColor {
        return UIColor(red: 235.0/255.0, green: 235.0/255.0, blue: 235.0/255.0, alpha: 1.0)
    }
    
    class func hgreyColor() -> UIColor {
        return UIColor(red: 183.0/255.0, green: 183.0/255.0, blue: 183.0/255.0, alpha: 1.0)
    }
    
    class func hDarkGreyColor() -> UIColor {
        return UIColor(red: 118.0/255.0, green: 116.0/255.0, blue: 117.0/255.0, alpha: 1.0)
    }
}